static void c2s_dgemm(const char transa, const char transb,
                      const int m, const int n, const int k,
                      const double alpha_, const __MD *a, const int lda,
                      const __MD *b, const int ldb,
                      const double beta_, __MD *c, const int ldc)
{
        int i, j, kp;
        const __MD *pa, *pb;
        __MD alpha = MM_SET1(alpha_);
        __MD beta = MM_SET1(beta_);
        __MD btmp[k];
        for (j = 0; j < n; j++) {
                if (beta_ == 0) {
                        memset(c, 0, sizeof(double)*m);
                } else {
                        for (i = 0; i < m; i++) {
                                c[i] *= beta;
                        }
                }
                if (transa == 'T') {
                        pb = b + ldb * j;
                        for (kp = 0; kp < k; kp++) {
                                btmp[kp] = alpha * pb[kp];
                        }
                        for (i = 0; i < m; i++) {
                                pa = a + lda * i;
                                for (kp = 0; kp < k; kp++) {
                                        c[i] += pa[kp] * btmp[kp];
                                }
                        }
                } else {
                        pb = b + ldb * j;
                        for (kp = 0; kp < k; kp++) {
                                btmp[0] = alpha * pb[kp];
                                pa = a + lda * kp;
                                for (i = 0; i < m; i++) {
                                        c[i] += pa[i] * btmp[0];
                                }
                        }
                }
                c += ldc;
        }
}


