static void mcopy_ij(__MD *opij, const __MD *gctr, 
                     const int ni, const int nj, const int mi, const int mj)
{
        int i, j;

        for (j = 0; j < mj; j++) {
                for (i = 0; i < mi; i++) {
                        opij[i] = gctr[i];
                }
                opij += ni;
                gctr += mi;
        }
}

void c2s_mset0(__MD *out, int *dims, int *counts)
{
        int ni = dims[0];
        int nj = dims[1];
        int nk = dims[2];
        int nij = ni * nj;
        int nijk = nij * nk;
        int i, j, k, l;
        __MD zero = MM_SET1(0);
        if (dims == counts) {
                for (i = 0; i < nijk * counts[3]; i++) {
                        out[i] = zero;
                }
                return;
        }
        int di = counts[0];
        int dj = counts[1];
        int dk = counts[2];
        int dl = counts[3];
        __MD *pout;
        for (l = 0; l < dl; l++) {
                for (k = 0; k < dk; k++) {
                        pout = out + k * nij;
                        for (j = 0; j < dj; j++) {
                        for (i = 0; i < di; i++) {
                                pout[j*ni+i] = zero;
                        } }
                }
                out += nijk;
        }
}

/*
 * 1e integrals, cartesian to real spheric.
 */
void c2s_sph_1e_simd(__MD *opij, __MD *gctr, int *dims,
                     CINTEnvVars *envs, double *cache)
{
        int i_l = envs->i_l;
        int j_l = envs->j_l;
        int i_ctr = envs->x_ctr[0];
        int j_ctr = envs->x_ctr[1];
        int di = i_l * 2 + 1;
        int dj = j_l * 2 + 1;
        int ni = dims[0];
        int nj = dims[1];
        int ofj = ni * dj;
        int nfi = envs->nfi;
        int nf = envs->nf;
        int ic, jc;
        int buflen = nfi*dj;
        __MD *buf1, *buf2;
        MALLOC_INSTACK(buf1, buflen);
        MALLOC_INSTACK(buf2, buflen);
        __MD *pij;
        __MD *tmp1;

        for (jc = 0; jc < j_ctr; jc++) {
        for (ic = 0; ic < i_ctr; ic++) {
                pij = opij + ofj * jc + di * ic;
                tmp1 = (c2s_ket_sph_simd[j_l])(buf1, gctr, nfi, nfi, j_l);
                tmp1 = (c2s_bra_sph_simd[i_l])(buf2, dj, tmp1, i_l);
                mcopy_ij(pij, tmp1, ni, nj, di, dj);
                gctr += nf;
        } }
}

/*
 * 1e integrals, reorder cartesian integrals.
 */
void c2s_cart_1e_simd(__MD *opij, __MD *gctr, int *dims,
                      CINTEnvVars *envs, double *cache)
{
        int i_ctr = envs->x_ctr[0];
        int j_ctr = envs->x_ctr[1];
        int nfi = envs->nfi;
        int nfj = envs->nfj;
        int nf = envs->nf;
        int ni = dims[0];
        int nj = dims[1];
        int ofj = ni * nfj;
        int ic, jc;
        __MD *popij;

        for (jc = 0; jc < j_ctr; jc++) {
        for (ic = 0; ic < i_ctr; ic++) {
                popij = opij + ofj * jc + nfi * ic;
                mcopy_ij(popij, gctr, ni, nj, nfi, nfj);
                gctr += nf;
        } }
}

